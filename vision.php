				<li>Joining a student group should not be so complicated. We will make the experience easier by developing a ‘Common App’ for recruitment and connecting students who are seeking involvement with groups that match their interests. </li>
				<li>Planning events is hard work, and we want to streamline the process. We will create a one-stop-shop website where student group leaders can find information about funding, access a centralized event calendar, and download resource guides just for them.</li>
				<li>Implementing a digital SOFO system will relieve 	burdens on student leaders. We envision a future where treasurers can submit transactions during non-business hours, advisors can approve requests online, and students can finally be reimbursed in a timely manner. This initiative may take more than one year, but we will work with SOFO to implement short-term changes (like online advisor approval) while a long-term system is designed and tested over the next year.</li>
