				<h3>Academic Stress</h3>
				<li>Northwestern requires more credits to graduate than any of its quarter system peers. We will advocate for a reevaluation of credit requirements in all six schools. Most immediately, we believe that the number of Weinberg graduation requirements should be reduced from 45 to 42 credits in order to decrease stress and increase academic flexibility.</li>
				<br>
				<h3>Mental Wellness</h3>
				<li>Faculty and staff often fail to recognize the academic and extracurricular burdens students face. Our university should prioritize student wellness, and the first of many steps would be to adopt a practice from UChicago by cancelling class one day each Winter Quarter so that students can focus on their well-being.</li>
				<li>Students should not have to choose between wellness and their responsibilities. We will work to expand and improve the accessibility of wellness programming (like Norris Exam Relief, CAPS workshops, and student group events) by creating a coalition of relevant groups and departments to centralize planning and advertising of wellness events across campus.</li>
				<li>In addition to responsive programming like workshops and Exam Relief, we must also place an emphasis on proactive mental wellness. In order to shift campus culture, we will launch a campus-wide wellness campaign to encourage students to take time for themselves and for others by actively planning downtime into their schedules.</li>
				<li>We should not have to wait so long to get the help we need, and wait lists for counseling are not acceptable. We will mobilize students to demand an increase in CAPS staff and services, and educate students about alternative resources in the interim.</li>
				<br>
				<h3>Sexual Assault</h3>
				<li>It is important for survivors of sexual assault and violence to have access to all of their care and response options, such as confidential counseling or a conduct report. We will create sexualassault.northwestern.edu to eliminate barriers to information and serve as the first stop for survivors looking to find resources, seek support, or report a concern. We will also create an online form where students can submit questions and receive answers from administrators--and more importantly, from students--in order to increase transparency of the university process. </li>
				<li>Peer intervention is a critical component in making campus safer. But not everyone knows when or how to proactively intervene, or how to respond when sexual violence has already occurred. We will use the ASG student group committees to connect interested student groups with resources and training from organizations such as MARS, SHAPE, HPAW and CARE.</li>
				<br>
				<h3>Alcohol Policy</h3>
				<li>Student well-being should be the greatest priority for any alcohol policy. We will push for a change to the Responsible Action Protocol so that callers and callees receive full amnesty and no one is punished for doing the right thing.</li>
				<li>Late-night, alcohol-free programming has been shown to reduce unsafe drinking behavior on college campuses. We will create a grant program for students and groups to plan late-night events during the off weeks of NU Nights programming.</li>
