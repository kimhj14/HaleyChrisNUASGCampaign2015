				<h1><a class="roll-link" name="community">Build Up: Inclusive Community</a></h1>
				<li>We will strive for a more critically engaged and inclusive community that is intentional about the kinds of dialogue that take place on campus, and we will work to create more spaces for these positive interactions to occur. The creation of a US-based Social Inequalities and Diversities Requirement in all six schools is crucial to this effort. This academic requirement would equip more students to foster an inclusive community outside of the classroom and reduce the unfair burden on marginalized students to educate others on their identities.</li>
				<li>Dialogue is important and necessary in order to gain new perspectives, but it can't stop there if we are to build a better, more critically engaged campus community. To that end, we will implement student leader trainings to begin this culture shift. We will also set aside ASG funds so that ideas such as group collaborations, new programs and campus events may become tangible realities. </li>
				<li>As if tuition wasn’t enough, the cost of course materials and other educational experiences can be discouragingly high. We will push administration to 1) subsidize class materials such as textbooks, supplies, laptops, and software for students who identify as low-income and 2) Increase aid for summer programs such as study abroad, Engage Chicago, and GESI to ensure that cost does not restrict educational opportunities.</li>
				<li>Access can make a big difference. We will work with Wildcat Welcome, the newly created Office of Student Enrichment Services, and student groups to better incorporate resources for students from low-income backgrounds before and during Wildcat Welcome. This will help to increase awareness of available resources and opportunities, and create a foundation for implementing new services, like funding for laptops and class materials.</li>
				<li>Academic advisors should know about more than just what happens in the classroom. We will work to increase training so that advisors are well-versed in opportunities such as scholarships and academic workshops to better support underrepresented students.</li>
				<li>Student events are at the heart of every Northwestern experience, and cost should never be a barrier to attend. We will work to increase the accessibility of campus events for students with financial need by creating a WildCARD scanner check-out program so that groups can accept vouchers and meal plan Cat Cash for event tickets.</li>
				<li>Living on-campus is a formative period for everyone, and students of all gender identities should have the option of living in a gender-open room in any building on campus.</li>
